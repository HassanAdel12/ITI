using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Lap_4
{
    class MyMath
    {
        

        #region operator two numbers

        public static double sum(double _num1, double _num2) 
        {
            return _num1 + _num2;
        }

        public static double sub(double _num1, double _num2)
        {
            return _num1 - _num2;
        }

        public static double mul(double _num1, double _num2)
        {
            return _num1 * _num2;
        }

        public static double div(double _num1, double _num2)
        {
            return _num1 / _num2;
        }

        public static double rem(double _num1, double _num2)
        {
            return _num1 % _num2;
        }

        #endregion


    }
}
